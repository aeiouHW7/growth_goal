export interface DailyReviewContext {
  todayPlans: string;
  userProfile: string;
  recentReviews: string;
}

export function buildDailyReviewPrompt(ctx: DailyReviewContext): string {
  return `
## 每日复盘流程

今日计划：
${ctx.todayPlans}

用户画像（精力分配/时间段）：
${ctx.userProfile}

近期复盘（最近 7 天，用于模式对照）：
${ctx.recentReviews}

请按以下步骤执行每日复盘：

### 阶段一：接收用户输入
1. 用户发送自由碎碎念或 "今天复盘"
2. 读取今日日计划（GET /api/plans/daily?date=today）
3. 读取用户画像

### 阶段二：结构化提取 + 信号深度评估
从用户碎碎念中提取：
- 完成了什么（completed）
- 没完成什么（notCompleted）
- 遇到了什么阻碍（obstacles）
- 情绪状态（emotionState）
- 心态信号（mindsetNote）

**信号深度检查**：8 维度加权评分，评估用户输入的"信号厚度"

| 维度 | 权重 | 评分标准 |
|------|:--:|------|
| 具体行为 | 3 | "我做了X"=3，"有人搞我"=1，完全无=0 |
| 归因指向 | 2 | "因为X"=2，"不知道"=0 |
| 情绪具体 | 2 | "焦虑担心OKR"=2，"烦"=0.5 |
| 时间锚定 | 2 | "今天下午"=2，无时间=0 |
| 涉及维度 | 1 | 提到目标维度=1，未提及=0 |
| 隐层意图 | 2 | 能推断出没说出来的=2，不能=0 |
| 防御机制 | -1 | 有防御机制(每种)扣0.5，上限-2 |
| 语言温度 | 1 | hot=1，warm=0.5，cool=0 |

**判定规则**：
- ≥8 极厚信号 → 直接分析，跳过追问
- 5-7 正常信号 → 继续下面流程
- 3-4 薄信号 → 缺口探测追问，最多 2 次
- <3 极薄信号 → 追问 1 次，无改善则标注 [极薄输入]

**缺口类型与追问方向**：
- [行为缺失] → 「你今天具体做了什么？」
- [归因缺失] → 「你觉得是什么原因造成的？」
- [情绪模糊] → 「你说的XX，具体是哪种感受？焦虑/无力/愤怒？」
- [时间缺失] → 「这是什么时候的事？」
- [隐层缺失] → 「你内心真正想要的是什么？」

**禁止空泛追问**：❌「你再多说说」❌「还有什么？」❌「详细说说？」

### 阶段三：追问（认知偏误感知 + 信息补全）
- 判断提取的信息是否充分覆盖今日计划
- **偏误检测**：留意用户表述中是否隐含以下偏误（含触发词、追问策略、干预方向）：

  | 偏误类型 | 触发词/句式 | 追问策略 | 干预方向 |
  |---------|------------|---------|---------|
  | **计划谬误** | "半小时就好""肯定能做完""很快搞定" | "你上次做类似的事花了多少时间？" | 用历史数据校准，不凭感觉 |
  | **自我美化** | "还行""差不多""还可以" | 追问具体数字/事实 | 要求量化，拒绝模糊表述 |
  | **基本归因错误** | "都怪他""运气不好""对手太强" | "在这个结果里，哪些是可控的？" | 区分可控与不可控因素 |
  | **确认偏误** | "你看，XXX也这么说" | "有没有不支持这个结论的数据？" | 主动寻找反面证据 |
  | **损失厌恶** | "万一失败了怎么办""不想放弃" | "如果失败了，实际损失是什么？" | 量化失败成本 vs 不做的机会成本 |
  | **事后合理化** | "当时只能那样做""我早知道会这样" | "当时做决定的理由是什么？" | 事前写决策依据，事后再对比 |
  | **现状偏差** | "算了就这样吧""反正习惯了" | "如果你从零开始选择，你会选现在的方案吗？" | 打破"默认选项"思维 |
  | **聚类错觉** | "然后就一直这样""每次都..." | "这个'规律'出现几次了？真的显著吗？" | 用基础概率评估事件 |
- 不足时追问，最多 2 次
- 第 3 次仍不足 → 标注 [部分信息缺失] 继续

### 阶段四：四层分析

调用 POST /api/analysis/generate 生成，每层具体要求：

1. **完成总结**：客观事实，不解释

2. **偏差分析**：与目标的差距，风险等级

3. **执行诊断**（含 Fogg 行为模型分析）：
   - 如果存在"知道该做但没做"的情况，用 Fogg 模型诊断：

     **动机（M）不足信号**："没动力""不想做""没意义" → 连接深层目的而非施压
     **能力（A）不足信号**："太难了""不会做""不知道怎么做" → 降低难度而非催促
     **提示（P）不足信号**："忘了""没想起来""没时间" → 增加提醒而非批评

   - 识别是否与近期复盘中的模式重复（对照近期复盘数据）
   - 根因归类：意愿不足/方法不对/外部阻力/目标不合理

4. **外部视角**：
   - 结合时事趋势审视方向
   - **机会成本视角**：今天的时间花在了哪里？是否有更高杠杆的用途？
     - 维护型（必须做）/ 增长型（推进目标）/ 消耗型（低价值耗散）
     - 如果消耗型占比过高，建议调整

### 阶段五：评分反馈
展示分析报告后，要求用户打分 (0-100)：
- score < 60：追问原因 → 记录到反思
- score ≥ 80：追问好在哪 → 记录到优秀案例
`.trim();
}
