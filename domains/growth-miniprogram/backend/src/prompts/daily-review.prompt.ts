export interface DailyReviewContext {
  todayPlans: string;
  userProfile: string;
  recentReviews: string;
}

export function buildDailyReviewPrompt(ctx: DailyReviewContext): string {
  return `
## 每日复盘流程

今日计划：
${ctx.todayPlans}

用户画像（精力分配/时间段）：
${ctx.userProfile}

近期复盘（最近 7 天，用于模式对照）：
${ctx.recentReviews}

请按以下步骤执行每日复盘：

### 阶段一：接收用户输入
1. 用户发送自由碎碎念或 "今天复盘"
2. 读取今日日计划（GET /api/plans/daily?date=today）
3. 读取用户画像

### 阶段二：结构化提取 + 信号深度评估
从用户碎碎念中提取：
- 完成了什么（completed）
- 没完成什么（notCompleted）
- 遇到了什么阻碍（obstacles）
- 情绪状态（emotionState）
- 心态信号（mindsetNote）

**信号深度检查**：评估用户输入的"信号厚度"
- 是否有具体行为描述（而非笼统概括）
- 是否有归因方向（内部/外部）
- 是否有具体情绪表达
- 是否有时间锚点

如果信号太薄（过于模糊），在追问中引导用户补充具体细节。

### 阶段三：追问（认知偏误感知 + 信息补全）
- 判断提取的信息是否充分覆盖今日计划
- **偏误检测**：留意用户表述中是否隐含以下偏误：
  - **计划谬误**："明天肯定能做完"但今天进度明显落后 → 追问具体时间安排
  - **自我美化**："还行""差不多"等模糊表述 → 追问具体数字/事实
  - **基本归因错误**："都是因为XX打断了我"但未提自己可以控制的部分 → 追问可控因素
  - **确认偏误**：只强调顺利的方面，回避困难的方面 → 温和提示反面视角
- 不足时追问，最多 2 次
- 第 3 次仍不足 → 标注 [部分信息缺失] 继续

### 阶段四：四层分析

调用 POST /api/analysis/generate 生成，每层具体要求：

1. **完成总结**：客观事实，不解释

2. **偏差分析**：与目标的差距，风险等级

3. **执行诊断**（含 Fogg 行为模型分析）：
   - 如果存在"知道该做但没做"的情况，用 Fogg 模型诊断：
     - **M（动机）**：用户是否真的想做？还是只是"应该做"？
     - **A（能力）**：任务是否太难/太大？能否再拆小？
     - **P（提示）**：是否有明确的行动触发点？
   - 识别是否与近期复盘中的模式重复（对照近期复盘数据）
   - 根因归类：意愿不足/方法不对/外部阻力/目标不合理

4. **外部视角**：
   - 结合时事趋势审视方向
   - **机会成本视角**：今天的时间花在了哪里？是否有更高杠杆的用途？
     - 维护型（必须做）/ 增长型（推进目标）/ 消耗型（低价值耗散）
     - 如果消耗型占比过高，建议调整

### 阶段五：评分反馈
展示分析报告后，要求用户打分 (0-100)：
- score < 60：追问原因 → 记录到反思
- score ≥ 80：追问好在哪 → 记录到优秀案例
`.trim();
}
