export interface CycleReviewContext {
  cycleType: "WEEKLY" | "MONTHLY";
  periodDescription: string;
  dailyReviews: string;
  missingDays: string;
  userProfile: string;
  goalProgress: string;
}

export function buildCycleReviewPrompt(ctx: CycleReviewContext): string {
  const isWeekly = ctx.cycleType === "WEEKLY";
  const checkEndpoint = isWeekly
    ? "GET /api/reviews/weekly/check?year=&week="
    : "GET /api/reviews/monthly/check?year=&month=";
  const createEndpoint = isWeekly
    ? "POST /api/reviews/weekly"
    : "POST /api/reviews/monthly";

  return `
## ${isWeekly ? "周" : "月"}复盘流程

周期：${ctx.periodDescription}

### 阶段一：检测触发
1. 调用 ${checkEndpoint} 检测是否需要触发
2. 如需触发，读取周期内所有复盘数据

### 阶段二：检查缺失
每日复盘数据：
${ctx.dailyReviews}

缺失天数：
${ctx.missingDays}

- 如有缺失天数 → 询问用户原因并记录到 missingDays

### 阶段三：聚合分析（含模式识别 + 认知偏误趋势 + 机会成本）

目标进度：
${ctx.goalProgress}

用户画像：
${ctx.userProfile}

生成完整分析，必须包含以下四个子层：

**1. 周期总结**
- 整体完成率
- 与上一周期对比（趋势方向）

**2. 模式识别（核心差异化）**
扫描周期内所有每日复盘，查找**频率 ≥ 3 次的重复模式**：

| 模式类型 | 举例 | 信号意义 |
|---------|------|---------|
| 重复阻碍 | 用户 3 次提到"会议干扰" | 系统性时间管理问题 |
| 重复情绪 | 多天出现"焦虑""疲惫" | 需关注心理状态 |
| 重复偏误 | 多次出现计划谬误/自我美化 | 思维习惯问题 |
| 重复亮点 | 多次提到某个高效时段 | 可复用的成功模式 |
| 沉默信号 | 某个目标维度一直未被提及 | 可能被忽视或回避 |

- 识别后清晰表述：这个${isWeekly ? "周" : "月"}，你 N 次提到「主题」，这已形成一个需要关注的模式
- 如果无频率 ≥ 3 的模式，注明"无明显重复模式"

   **矛盾触发** — 检测用户言行不一：
   - 自我评估 vs 行为表现：用户说"我执行力没问题"但多次计划未完成
   - 目标 vs 行动：用户说"我要..."但周期内无任何相关行动记录
   - 认知 vs 执行：用户说"我知道该做"但无主动改变记录
   - 触发后清晰表述：「你说的A，但你的行为是B，这两件事放在一起看…」

   **静默触发** — 某个目标维度连续 2 个周期未被提及 → 触发"你在回避还是真的忘了？"

**3. 认知偏误趋势**
回顾周期内的决策和判断，检测：
- 是否持续存在某种偏误（如每周都低估任务时间）
- 偏误是否在用户意识到后有所改善（对照反馈记录）
- 给出具体的改进建议

**4. 外部视角 + 机会成本分析**
- 搜索当前时事趋势，结合目标方向审视
  - 如外部搜索失败 → 标注 [外部信息获取失败]，不阻塞分析
- **时间配置审计**：将周期内的时间按三类归类：
  - 🟢 增长型（直接推进目标，如学习核心技能/创作内容/建立人脉/健身）
  - 🟡 维护型（必要但不推进目标，如正常工作/家务/通勤）
  - 🔴 消耗型（低价值耗散，如无目的刷手机/无效社交/纯娱乐）
  - 计算 **战略评分 = 增长占比 - 消耗占比**：
    - >30% 优秀 | 10-30% 良好 | 0-10% 及格 | <0% 危险
  - **问题类型诊断**：
    - **穷忙族** — 维护型 >70%，没时间成长 → 建议砍掉低价值维护事项
    - **消费主义** — 消耗型 >30% → 建议设定娱乐时间上限
    - **虚假勤奋** — 做了很多"成长"的事，但与目标无关 → 建议对齐目标
    - **方向迷失** — 成长时间够但方向不对 → 建议重新审视目标优先级
- **Fogg 趋势分析**：如果某个执行问题反复出现，判断是 M/A/P 中哪个要素持续不足，按 Coach 规则干预：
  - 持续动机不足 → 连接深层目的而非施压：「这个目标对你来说真正意味着什么？」
  - 持续能力不足 → 降低难度而非催促：「能否把这个任务拆到不能再小？」
  - 持续提示缺失 → 增加提醒而非批评：「什么样的环境触发能帮你记起来？」

### 阶段四：用户补充
1. 展示聚合报告
2. 用户可补充个人感受/细节
3. 调用 ${createEndpoint} 保存

### 阶段五：评分反馈
- 用户打分 (0-100)
- < 60 记录反思，≥ 80 记录优秀案例
`.trim();
}
